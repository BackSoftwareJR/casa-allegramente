import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import React from 'react';
import { renderToStaticMarkup } from 'react-dom/server';
import matter from 'gray-matter';
import readingTime from 'reading-time';
import { serialize } from 'next-mdx-remote/serialize';
import * as jsxRuntime from 'react/jsx-runtime';
import { buildMdxComponents } from './blog-mdx-components.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const BLOG_DIR = path.join(__dirname, '../content/blog');
const OUT_FILE = path.join(__dirname, '../src/lib/blog-data.generated.ts');

const jsxDevRuntime = {
  ...jsxRuntime,
  jsxDEV: jsxRuntime.jsxDEV ?? jsxRuntime.jsx,
  useMDXComponents: () => ({}),
};

async function renderMdxToHtml(source) {
  const { compiledSource, scope } = await serialize(source, { parseFrontmatter: false });
  const fullScope = { opts: jsxDevRuntime, ...scope };
  const keys = Object.keys(fullScope);
  const values = Object.values(fullScope);
  const hydrateFn = Reflect.construct(Function, keys.concat(compiledSource));
  const result = hydrateFn.apply(hydrateFn, values);
  const MDXContent = result.default;

  return renderToStaticMarkup(
    React.createElement(MDXContent, { components: buildMdxComponents }),
  );
}

function parsePost(filename) {
  const raw = fs.readFileSync(path.join(BLOG_DIR, filename), 'utf8');
  const { data, content } = matter(raw);
  const stats = readingTime(content);

  return {
    slug: data.slug,
    title: data.title,
    description: data.description,
    keywords: data.keywords ?? [],
    category: data.category,
    date: data.date,
    lastModified: data.lastModified ?? data.date,
    author: data.author,
    image: data.image,
    imageAlt: data.imageAlt,
    readingTime: data.readingTime ?? `${Math.ceil(stats.minutes)} min`,
    featured: Boolean(data.featured),
    content,
  };
}

async function main() {
  if (!fs.existsSync(BLOG_DIR)) {
    throw new Error(`Missing blog content directory: ${BLOG_DIR}`);
  }

  const files = fs.readdirSync(BLOG_DIR).filter((f) => f.endsWith('.mdx'));
  const posts = [];

  for (const filename of files) {
    const post = parsePost(filename);
    const html = await renderMdxToHtml(post.content);
    posts.push({ ...post, html });
  }

  posts.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());

  const output = `/* eslint-disable */
// Generated by scripts/generate-blog-data.mjs — do not edit manually.
import type { BlogPost } from './blog-types';

export const BLOG_POSTS: BlogPost[] = ${JSON.stringify(posts, null, 2)} as BlogPost[];
`;

  fs.writeFileSync(OUT_FILE, output, 'utf8');
  console.log(`Generated ${posts.length} blog posts → src/lib/blog-data.generated.ts`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
